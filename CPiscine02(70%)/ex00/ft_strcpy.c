char	*ft_strcpy(char *dest, char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		dest[cnt] = str[cnt];
		cnt++;
	}
	dest[cnt] = '\0';
	return (dest);
}
